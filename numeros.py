# Operaciones con números enteros
suma = 1 + 1
resta = 1 - 1
division = 1 / 1
multiplicacion = 1 * 1
print (suma)
print (resta)
print (division)
print (multiplicacion)

# Numeros flotantes
suma = 1.1 + 1.1
resta = 1.1 - 1.1
division = 1.1 / 1.1
multiplicacion = 1.1 * 1.1
print (suma)
print (resta)
print (division)
print (multiplicacion)

# Guiones en números largos
numero_largo = 1_000_000
print (numero_largo) # Solo imprimirá el número sin guiones

